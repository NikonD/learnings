// Тема 6: Форматирование строк (шаблонные литералы и конкатенация)
// Запуск: node 06_string_formatting.js

// В JS удобный способ — шаблонные литералы (обратные кавычки ` ` и ${}).

// ---------------------------------------------------------------------
// Шаблонные литералы (template literals)
// ---------------------------------------------------------------------
let userName = "Alex";
let userAge = 21;
console.log(`Имя: ${userName}, возраст: ${userAge}`);
// Имя: Alex, возраст: 21

// В ${} можно писать любое выражение
let a = 5;
let b = 3;
console.log(`${a} + ${b} = ${a + b}`);


// ---------------------------------------------------------------------
// Форматирование чисел (через .toFixed и т.д.)
// ---------------------------------------------------------------------
let price = 1999.9567;
console.log("Цена:", price);
console.log("Цена (2 знака):", price.toFixed(2));

// ---------------------------------------------------------------------
// Конкатенация строк через +
// ---------------------------------------------------------------------
console.log("Имя: " + userName + ", возраст: " + String(userAge));
// Шаблонные литералы обычно удобнее

// ---------------------------------------------------------------------
// Многострочные строки (только в ` `)
// ---------------------------------------------------------------------
let multiline = `Строка 1
Строка 2
Строка 3`;
console.log(multiline);

// ---------------------------------------------------------------------
// Экранирование в обычных строках: \n, \t, \"
// ---------------------------------------------------------------------
console.log("Первая строка\nВторая строка");
console.log("Табуляция\tздесь");
console.log('Кавычки внутри: "привет"');