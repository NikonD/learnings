// В JS переменные объявляют через let, const (или устаревший var).
// Тип определяется по значению (динамическая типизация).

// ---------------------------------------------------------------------
// number — и целые, и дробные (в JS нет отдельного int/float)
// ---------------------------------------------------------------------
let a = 10;
let b = -3;
let pi = 3.14;
let temp = -0.5;

// ---------------------------------------------------------------------
// string — строки (одинарные, двойные или обратные кавычки)
// ---------------------------------------------------------------------
let name = "Nikon";
let city = "Petropavlovsk";
let same = "Petropavlovsk";

// ---------------------------------------------------------------------
// boolean — логика: true / false (строчная буква!)
// ---------------------------------------------------------------------
let isOk = true;
let hasErrors = false;

console.log(a);
// 10
console.log("a =", a, "| type:", typeof a);
// a = 10 | type: number
console.log("pi =", pi, "| type:", typeof pi);
console.log("name =", name, "| type:", typeof name);
console.log("isOk =", isOk, "| type:", typeof isOk);

// ---------------------------------------------------------------------
// Преобразование типов (кастинг)
// ---------------------------------------------------------------------
console.log("Number('12') =", Number("12"));
console.log("parseInt('12', 10) =", parseInt("12", 10));
console.log("parseFloat('12.5') =", parseFloat("12.5"));
console.log("String(100) =", String(100));
console.log("Boolean(0) =", Boolean(0), "| Boolean(10) =", Boolean(10));

// Частая ошибка новичков:
// Number('12.5') даёт 12.5; parseInt('12.5') даёт 12 (целая часть).
console.log("parseInt('12.5') =", parseInt("12.5"));
console.log("Number('привет') =", Number("привет")); // NaN (Not a Number)

// ---------------------------------------------------------------------
// const — константа (значение нельзя переназначить)
// ---------------------------------------------------------------------
const MAX = 100;
// MAX = 200;  // ошибка в строгом режиме
let counter = 0;
counter = 1; // так можно

// Пустой объект и массив можно менять «внутри», но не переприсваивать.
const arr = [1, 2, 3];
arr.push(4);   // можно
// arr = [5, 6]; // ошибка — переприсваивание
