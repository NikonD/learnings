// 05_loops.js
// Тема 5: Циклы (for, while, for...of, for...in)
// Запуск: node 05_loops.js

// Цикл — повторение блока кода несколько раз.

// ---------------------------------------------------------------------
// for (классический) — когда известны начало, конец и шаг
// ---------------------------------------------------------------------
for (let i = 0; i < 5; i++) {
  console.log("i =", i);
}
// i = 0, 1, 2, 3, 4

for (let i = 2; i < 10; i += 2) {
  console.log("even i =", i);
}
// 2, 4, 6, 8

// Подсчёт суммы
let total = 0;
for (let i = 1; i <= 5; i++) {
  total += i;
}
console.log("Сумма чисел от 1 до 5:", total);

// ---------------------------------------------------------------------
// while — повторяем, пока условие true
// ---------------------------------------------------------------------
let count = 5;
while (count >= 1) {
  console.log("count =", count);
  count--;
}

// ---------------------------------------------------------------------
// break — немедленный выход из цикла
// ---------------------------------------------------------------------
for (let i = 1; i < 10; i++) {
  if (i === 6) break;
  console.log("before break:", i);
}

// ---------------------------------------------------------------------
// continue — пропуск текущей итерации
// ---------------------------------------------------------------------
for (let i = 1; i < 7; i++) {
  if (i === 4) continue;
  console.log("after continue:", i);
}

// ---------------------------------------------------------------------
// for...of — перебор значений массива (или другой итерируемой коллекции)
// ---------------------------------------------------------------------
const fruits = ["apple", "banana", "cherry"];
for (const fruit of fruits) {
  console.log("fruit:", fruit);
}

// Индекс через entries() или вручную
fruits.forEach((fruit, index) => {
  console.log("index =", index, ", fruit =", fruit);
});

// ---------------------------------------------------------------------
// for...in — перебор ключей объекта (для массивов лучше for...of)
// ---------------------------------------------------------------------
const user = { name: "Alex", age: 20 };
for (const key in user) {
  console.log(key, "->", user[key]);
}