// Условие — проверка, результат true или false.
// В зависимости от результата выполняется нужный блок кода.

// ---------------------------------------------------------------------
// Операторы сравнения
// ---------------------------------------------------------------------
// ==  равно (с приведением типов)
// === строго равно (тип и значение)
// !=  не равно
// !== строго не равно
// >   больше
// <   меньше
// >=  больше или равно
// <=  меньше или равно

let a = 10;
let b = 5;

console.log("a === b:", a === b);
console.log("a !== b:", a !== b);
console.log("a > b:", a > b);
console.log("a < b:", a < b);

// Важно: 5 == "5" даёт true, 5 === "5" даёт false. Обычно используют ===.
console.log('5 == "5":', 5 == "5");
console.log('5 === "5":', 5 === "5");
// 1 + '1' = "11"
// 1 - '1' = 0
console.log("1 + '1' =", 1 + '1');
console.log("1 - '1' =", 1 - '1');

// ---------------------------------------------------------------------
// if — выполняется, если условие true
// ---------------------------------------------------------------------
let x = 7;
if (x > 0) {
  console.log("Число положительное");
}

// ---------------------------------------------------------------------
// if / else — выбор из двух вариантов
// ---------------------------------------------------------------------
let y = -3;
if (y >= 0) {
  console.log("Неотрицательное число");
} else {
  console.log("Отрицательное число");
}

// ---------------------------------------------------------------------
// if / else if / else — несколько вариантов
// ---------------------------------------------------------------------
let score = 82;
let grade;

if (score >= 90) {
  grade = "Отлично";
} else if (score >= 70) {
  grade = "Хорошо";
} else if (score >= 50) {
  grade = "Удовлетворительно";
} else {
  grade = "Неудовлетворительно";
}

console.log("Оценка:", grade);

// ---------------------------------------------------------------------
// Логические операторы: && (и), || (или), ! (не)
// ---------------------------------------------------------------------
let age = 19;
let hasTicket = true;

if (age >= 18 || hasTicket) {
  console.log("Вход разрешён");
} else {
  console.log("Вход запрещён");
}

let isAdmin = true;
if (!isAdmin) {
  console.log("Обычный пользователь");
}

// ---------------------------------------------------------------------
// Тернарный оператор: условие ? значениеЕслиTrue : значениеЕслиFalse
// ---------------------------------------------------------------------
let num = 4;
let parity = num % 2 === 0 ? "чётное" : "нечётное";

// result = a if a > b else b

console.log("parity =", parity);