
# 1. Запаковка данных в tuple (кортеж)

# Функция для создания кортежа (tuple)
def create_tuple(name, age):
    """
    Создает кортеж с данными.
    
    :param name: Имя.
    :param age: Возраст.
    :return: Кортеж с данными.
    """
    return (name, age)  # Возвращаем кортеж с именем и возрастом

# Пример использования функции
person_tuple = create_tuple("Ivan", 23)
print("Запакованный кортеж:", person_tuple)  # Печатаем кортеж


# 2. Распаковка данных из tuple

# Функция для распаковки кортежа в отдельные переменные
def unpack_tuple(person_tuple):
    """
    Распаковывает кортеж в переменные.
    
    :param person_tuple: Кортеж с данными.
    :return: Имя и возраст.
    """
    name, age = person_tuple  # Распаковываем кортеж в переменные
    return name, age  # Возвращаем распакованные значения

# Пример использования функции
name, age = unpack_tuple(person_tuple)
print("Распакованные данные: Name:", name, ", Age:", age)  # Печатаем распакованные данные


# 3. Запаковка в список

# Функция для создания списка из данных
def create_list(name, age):
    """
    Создает список из данных.
    
    :param name: Имя.
    :param age: Возраст.
    :return: Список с данными.
    """
    return [name, age]  # Возвращаем список с именем и возрастом

# Пример использования функции
person_list = create_list("Ivan", 23)
print("Запакованный список:", person_list)  # Печатаем список


# 4. Распаковка данных из списка

# Функция для распаковки списка в переменные
def unpack_list(person_list):
    """
    Распаковывает список в переменные.
    
    :param person_list: Список с данными.
    :return: Имя и возраст.
    """
    name, age = person_list  # Распаковываем список в переменные
    return name, age  # Возвращаем распакованные значения

# Пример использования функции
name, age = unpack_list(person_list)
print("Распакованные данные из списка: Name:", name, ", Age:", age)  # Печатаем распакованные данные
