
import json

# 1. Запись данных в JSON файл

# Функция для записи данных в файл в формате JSON
def write_to_json(data, filename):
    """
    Записывает данные в файл в формате JSON.
    
    :param data: Данные, которые нужно записать (обычно это словарь или список).
    :param filename: Имя файла, в который будут записаны данные.
    """
    # Открываем файл для записи. Если файла нет, он будет создан.
    # json.dump() преобразует данные в формат JSON и записывает в файл.
    with open(filename, "w") as json_file:
        json.dump(data, json_file)
        print(f"Данные успешно записаны в файл {filename}")  # Печатаем сообщение о успешной записи

# Пример использования функции
data = {
    "name": "Ivan",  # Имя
    "age": 23,       # Возраст
    "is_student": True  # Студент ли он?
}

# Записываем данные в файл "data.json"
write_to_json(data, "data.json")


# 2. Чтение данных из JSON файла

# Функция для чтения данных из JSON файла
def read_from_json(filename):
    """
    Читает данные из файла в формате JSON.
    
    :param filename: Имя файла, из которого нужно прочитать данные.
    :return: Данные, прочитанные из файла (обычно это словарь или список).
    """
    # Открываем файл для чтения. В случае отсутствия файла будет ошибка.
    # json.load() загружает данные из файла в Python-объект (например, в словарь).
    with open(filename, "r") as json_file:
        data = json.load(json_file)
        return data  # Возвращаем данные

# Пример использования функции
loaded_data = read_from_json("data.json")
print("Данные из JSON файла:", loaded_data)  # Печатаем данные, которые были считаны из файла
