
import re  # Подключаем модуль для работы с регулярными выражениями

# 1. Разделение строки с помощью метода split()

# Функция для разделения строки по пробелам
def split_string(text):
    """
    Разделяет строку по пробелам и возвращает список слов.
    
    :param text: Строка, которую нужно разделить.
    :return: Список слов.
    """
    return text.split()  # Разделяет строку по пробелам и возвращает список

# Пример использования функции
text = "Hello, world! Welcome to Python."
split_text = split_string(text)
print("Результат split:", split_text)  # Выводим результат


# 2. Замена подстроки с помощью метода replace()

# Функция для замены одного слова на другое
def replace_substring(text, old, new):
    """
    Заменяет все вхождения подстроки old на подстроку new в строке text.
    
    :param text: Исходная строка.
    :param old: Подстрока, которую нужно заменить.
    :param new: Подстрока, на которую нужно заменить.
    :return: Измененная строка.
    """
    return text.replace(old, new)  # Заменяет все вхождения old на new

# Пример использования функции
new_text = replace_substring(text, "world", "everyone")
print("Результат replace:", new_text)  # Печатаем результат замены


# 3. Регулярные выражения с помощью модуля re

# Функция для поиска всех слов, начинающихся с буквы "W"
def find_words_starting_with_W(text):
    """
    Находит все слова в строке, начинающиеся с буквы 'W' с помощью регулярного выражения.
    
    :param text: Исходная строка.
    :return: Список слов, начинающихся с 'W'.
    """
    pattern = r"\bW\w+"  # Регулярное выражение для слов, начинающихся с "W"
    return re.findall(pattern, text)  # Ищет все совпадения по шаблону и возвращает их в виде списка

# Пример использования функции
matches = find_words_starting_with_W(text)
print("Слова, начинающиеся с 'W':", matches)  # Печатаем найденные слова
